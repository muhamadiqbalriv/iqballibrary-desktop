    private void insertPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_insertPeminjamanMouseClicked
        // TODO add your handling code here:
        Object id_anggota = ((ComboItem)id_anggotas.getSelectedItem()).getValue();
        Object id_pegawai =  ((ComboItem)id_pegawais.getSelectedItem()).getValue();
        java.util.Date tanggal = tgl_kunjungans.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tgl_kunjungan = sdf.format(tanggal);
        
        String tgl_pengembalian = "";
        String tujuan = tujuans.getText();
        
        String queryInsert = "INSERT INTO kunjungan(id_anggota,id_pegawai,tgl_kunjungan,tujuan)VALUES(?,?,?,?)";
        
        try {
            pst = koneksi.prepareStatement(queryInsert);
            pst.setObject(1, id_anggota);
            pst.setObject(2, id_pegawai);
            pst.setString(3, tgl_kunjungan);
            pst.setString(4, tujuan);
            if(id_pegawai.equals("")){
                JOptionPane.showMessageDialog(null, "Formulir belum lengkap !");
            }else{
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data tersimpan !");
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog (null, "Tambah data lagi ?","Warning",dialogButton);
                if (dialogResult == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null, "Data tersimpan !");
                id_anggotas.requestFocus();
                id_pegawais.requestFocus();
                tgl_kunjungans.setDate(tanggal);
                tujuans.setText(null);
                }else{
                    indexKunjungan iP = new indexKunjungan();
                    iP.setVisible(true);
                    this.setVisible(false);
                }
            }
        }catch (SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_insertPeminjamanMouseClicked

