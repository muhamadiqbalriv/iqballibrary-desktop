    private void comboAnggota(){
        try{
            String bacaData = "SELECT * from anggota ;";
            pst = koneksi.prepareStatement(bacaData);
            res = pst.executeQuery();
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            while (res.next()){
                model.addElement(new ComboItem(res.getString("id_anggota"),res.getString("nama")));
            }
            id_anggotas.setModel(model);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
