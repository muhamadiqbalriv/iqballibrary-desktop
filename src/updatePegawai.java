    private void updateAnggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateAnggotaMouseClicked
        // TODO add your handling code here:
        String id_pegawai = ambilId;
        String no_identitas = no_identitass.getText();
        String jenis_identitas = (String)jenis_identitass.getSelectedItem();
        String nama = namas.getText();
        String jenis_kelamin = "";
        if(rdLaki.isSelected()){
             jenis_kelamin = "Laki-laki";
        }else{
             jenis_kelamin = "Perempuan";
        }
        String alamat = alamats.getText();
        String no_hp = no_hps.getText();
        String images = "";
        if(imgPath != null){
            images = ", gambar=?";
        }
        
        String queryUpdate = "UPDATE pegawai SET no_identitas='"+no_identitas+"',jenis_identitas='"+jenis_identitas+"',nama='"+nama+"',jenis_kelamin='"+jenis_kelamin+"', alamat='"+alamat+"',no_hp='"+no_hp+"' "+images+" WHERE id_pegawai ='"+id_pegawai+"'";
        try {
            if(imgPath != null){
                InputStream  img = new FileInputStream(imgPath);
                pst =  koneksi.prepareStatement(queryUpdate);
                pst.setBlob(1,img);
            }else{
                pst =  koneksi.prepareStatement(queryUpdate);
            }
            pst.execute();
            JOptionPane.showMessageDialog(null, "Berhasil diupdate !");
        }catch (SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, e);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(updatePegawai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateAnggotaMouseClicked

