    public void dataTampil(){
        try{
            String bacaData = "SELECT * FROM pegawai WHERE id_pegawai="+ambilId;
            pst = koneksi.prepareStatement(bacaData);
            res = pst.executeQuery();
            if(res.next()){
                no_identitass.setText(res.getString("no_identitas"));
                jenis_identitass.setSelectedItem(res.getString("jenis_identitas"));
                namas.setText(res.getString("nama"));
                alamats.setText(res.getString("alamat"));
                no_hps.setText(res.getString("no_hp"));
                byte[] Passimg = res.getBytes("gambar");
                gambar.setIcon(ResizeImage(null, res.getBytes("gambar")));
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

